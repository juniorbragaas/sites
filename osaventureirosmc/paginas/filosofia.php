Temos como objetivo principal difundir nossa paixão. Que é o motociclismo, e como tal, não discriminamos nenhuma marca de motocicleta. Em nosso clube teremos Harley-Davidson's, Kawasaki's, Honda's, Yamaha's, Suzuki's, BMW's e outras. Não há também discriminação de idade, estado civil ou social. Exigimos apenas respeito mútuo. Tentamos fazer com que nosso clube seja respeitado como continuação de nossa família. Ajudamo-nos da melhor maneira possível, pois sabemos que somente com UNIÃO poderemos fazer uma associação forte. Nosso trabalho não visa rendimentos financeiros para os sócios, pois nossa associação não tem fins lucrativos. Todos os sócios têm como obrigação fazer OS AVENTUREIROS um grande moto clube, não no sentido de número de sócios, mas sim no sentido de qualidade, afinal é para este grupo que levamos nossas esposas, filhos, amigos, namoradas, etc...
<br><br>

A vida em duas rodas é muito prazerosa, pois estamos sempre em contato com muitas pessoas, constantemente se conhecem mais pessoas novas e diferentes. Não se podem confundir os motociclistas com algumas pessoas com atitudes isoladas, pois muitas vezes somos comparados com pessoas que estão sempre de preto, bêbados e arrumando encrenca, isto é lógico que existe, mas é muito mais coisa de filme americano que realidade. Os motociclistas são uma família muito unida que quer se encontrar, o caso das roupas pretas é uma coincidência devido às roupas de couro, só que hoje se forem reparar há roupas de todas as cores. Outro fato é que em sua grande maioria os motociclistas são pessoas normais, chefes de família, com emprego fixo e em sua maioria, muito bem sucedido.
<br><br>

OS AVENTUREIROS compreendem que existem diferenças de pensamentos e de perfis de comportamento, mesmo em relação a outros motos clubes. Mas dentro do espírito de tolerância e de irmandade que une todos os motociclistas.
